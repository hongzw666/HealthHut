package com.china.HealthHut.pojo;

public class Drugdetail {
	private String dd_id;
	private String drug_no;
	private String name;
	private String unit;
	private String min_unit;
	private String stock_num;
	private String effective_date;
	private String update_date;
	private String operator;
	public String getDd_id() {
		return dd_id;
	}
	public void setDd_id(String dd_id) {
		this.dd_id = dd_id;
	}
	public String getDrug_no() {
		return drug_no;
	}
	public void setDrug_no(String drug_no) {
		this.drug_no = drug_no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getMin_unit() {
		return min_unit;
	}
	public void setMin_unit(String min_unit) {
		this.min_unit = min_unit;
	}
	public String getStock_num() {
		return stock_num;
	}
	public void setStock_num(String stock_num) {
		this.stock_num = stock_num;
	}
	public String getEffective_date() {
		return effective_date;
	}
	public void setEffective_date(String effective_date) {
		this.effective_date = effective_date;
	}
	public String getUpdate_date() {
		return update_date;
	}
	public void setUpdate_date(String update_date) {
		this.update_date = update_date;
	}
	public String getOperator() {
		return operator;
	}
	public void setOperator(String operator) {
		this.operator = operator;
	}
	
}
